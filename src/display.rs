use std::io::{self, Write};
use crate::train::Train;

pub fn draw(){
    const ROW : usize = 60;
    const COL : usize = 7;

    let mut track: Vec<Vec<String>> = vec![vec!["=".to_string(); ROW]; COL];

    for (i, lane) in track.iter().enumerate() {
        print!("Lane {:02} [", i);

        for segment in lane {
            print!("{}", segment);
        }
        println!("]");
    }
}

