use std::io::{self, Write};

pub fn draw(track: &Vec<Vec<String>>){
    clearscreen::clear().expect("failed to clear screen");
    for row in track {
        print!("{}", "Lane [ ");
        for cell in row {
            print!("{}", cell)
        }
        println!(" ]");
    }
}

